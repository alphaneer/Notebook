### 用好印象笔记的二八法则

用好影响笔记，掌握如下4点。

#### 收集

把所有的信息全部同步到一个地方，而不是散落在各个小地方。

####搜索

收集的目的是为了搜索，不同于去互联网漫无目的搜索，你只是想看自己以前相关的记录。

印象笔记发家于文本识别，它不但能够检索文本，还可以把附件的PDF，图片里的内容也检索

